class TSSABlock(nn.Module):
    def __init__(self, c, num_heads=4, shortcut=True) -> None:
        """Initializes the TSSABlock with attention and feed-forward layers for enhanced feature extraction."""
        super().__init__()

        self.norm = DynamicTanh(c, False, 0.5)

        self.attn = AttentionTSSA(c)
        self.ffn = nn.Sequential(Conv(c, c * 2, 1), Conv(c * 2, c, 1, act=False))
        self.add = shortcut

    def forward(self, x):
        """Executes a forward pass through TSSABlock, applying attention and feed-forward layers to the input tensor."""
        B, C, H, W = x.shape
        # x = self.norm(x)
        # 原始形状为(B, C, H, W)
        x = x.view(B, C, -1).permute(0, 2, 1) # 合并后两个维度: (B, C, H*W),交换最后两个维度: (B, H*W, C)
        x = x + self.attn(x) if self.add else self.attn(x)
        x = x.permute(0, 2, 1).view(B, C, H, W)
        x = x + self.ffn(x) if self.add else self.ffn(x)
        return x

class DynamicTanh(nn.Module):
    def __init__(self, normalized_shape, channels_last, alpha_init_value=0.5):
        # normalized_shape：定义了 weight 和 bias 的形状，类似于 nn.LayerNorm 的归一化维度。
        # channels_last：布尔值，指示数据的通道维度是否在最后。如果为 True，则假设输入张量的形状为 (N, H, W, C)；如果为 False，则假设形状为 (N, C, H, W)。
        # alpha_init_value：初始化参数 alpha 的值，默认为 0.5。
        super().__init__()
        self.normalized_shape = normalized_shape
        self.alpha_init_value = alpha_init_value
        self.channels_last = channels_last

        self.alpha = nn.Parameter(torch.ones(1) * alpha_init_value)  # 一个标量参数，用于控制 torch.tanh 的缩放。
        self.weight = nn.Parameter(torch.ones(normalized_shape))  # 一个张量，形状为 normalized_shape，用于对输入张量进行逐元素乘法操作。
        self.bias = nn.Parameter(torch.zeros(normalized_shape))  # 一个张量，形状为 normalized_shape，用于对输入张量进行逐元素加法操作。

    def forward(self, x):
        x = torch.tanh(self.alpha * x)
        if self.channels_last:
            x = x * self.weight + self.bias
        else:
            x = x * self.weight[:, None, None] + self.bias[:, None,
                                                 None]  # 通过广播机制（[:, None, None]）将 weight 和 bias 应用于每个通道。
        return x

    def extra_repr(self):  # 在打印模块时显示额外信息
        return f"normalized_shape={self.normalized_shape}, alpha_init_value={self.alpha_init_value}, channels_last={self.channels_last}"

